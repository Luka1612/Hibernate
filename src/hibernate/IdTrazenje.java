    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Session session = HibernateUtil.createSessionFactory().openSession();
        Transaction tx = null;
        tx = session.beginTransaction();

        String id = textField1.getText();
        int id2 = Integer.parseInt(id);

        Person person = (Person) session.get(Person.class, id2);
        textField2.setText(String.valueOf(person.getIme()));
        textField3.setText(String.valueOf(person.getBrojgodina()));
        textField4.setText(String.valueOf(person.getAdresa()));
        textField5.setText(String.valueOf(person.getDohotak() + "$"));

        tx.commit();
    }//GEN-LAST:event_jButton2ActionPerformed

