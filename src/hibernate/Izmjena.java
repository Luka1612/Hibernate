    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        try {
            Session session = HibernateUtil.createSessionFactory().openSession();
            Transaction tx = null;
            tx = session.beginTransaction();

            String id = textField1.getText();
            int id2 = Integer.parseInt(id);
            String ime = textField2.getText();
            String brojgodina = textField3.getText();
            int brojgodina2 = Integer.parseInt(brojgodina);
            String adresa = textField4.getText();
            String dohotak = textField5.getText();
            int dohotak2 = Integer.parseInt(dohotak);

            Person person = (Person) session.get(Person.class, id2);
            person.setIme(ime);
            person.setBrojgodina(brojgodina2);
            person.setAdresa(adresa);
            person.setDohotak(dohotak2);

            session.update(person);
            tx.commit();
            textField6.setText("Uspješno");

        } catch (Exception e) {
            textField6.setText("Greška: " + e);
        }


    }//GEN-LAST:event_button2ActionPerformed

